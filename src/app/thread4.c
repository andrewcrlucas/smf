/* module */
#include "thread4.h"
/* os */
#include "cmsis_os2.h"
#include "os_mutex.h"

/******************************************************************************
  Public functions
******************************************************************************/

/**************************************************************************//**

  @brief

  @param void

  @return void

******************************************************************************/
void thread4(void *me)
{
    while (1)
    {
        /* Give up processor */
        osThreadYield();

        osMutexAcquire(g_my_resource_mutex_id, osWaitForever);

        /* use resource for a period of time */
        osDelay(5);

        osMutexRelease(g_my_resource_mutex_id);
    }
}

